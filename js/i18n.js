const LANG_KEY = 'sentiero.lang'
let currentLang = 'de'

try {
  const saved = localStorage.getItem(LANG_KEY)
  if (saved && DICT[saved]) currentLang = saved
} catch (e) {}

export function getLang () { return currentLang }
export function listLangs () { return Object.keys(DICT) }

export function setLang (lang) {
  if (!DICT[lang]) return
  currentLang = lang
  try { localStorage.setItem(LANG_KEY, lang) } catch (e) {}
  document.documentElement.lang = lang
  applyDomTranslations()
  document.dispatchEvent(new CustomEvent('lang:change', { detail: lang }))
}

export function t (key, vars) {
  const raw = (DICT[currentLang] && DICT[currentLang][key]) || (DICT.de[key] || key)
  if (!vars) return raw
  return raw.replace(/\{(\w+)\}/g, (_, k) => vars[k] != null ? vars[k] : '')
}

export function applyDomTranslations () {
  document.documentElement.lang = currentLang
  document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n) })
  document.querySelectorAll('[data-i18n-html]').forEach(el => { el.innerHTML = t(el.dataset.i18nHtml) })
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = t(el.dataset.i18nPlaceholder) })
  document.querySelectorAll('[data-i18n-aria]').forEach(el => { el.setAttribute('aria-label', t(el.dataset.i18nAria)) })
}
